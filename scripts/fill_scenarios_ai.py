# 3. إدخال السيناريوهات إلى Supabase
# ==========================================
def process_and_save_scenarios():
    print("🚀 جاري رفع سيناريوهات العملاء إلى قاعدة المتجهات (Vector DB)...")
    success_count = 0

    if RESET_SCENARIO_DOCUMENTS:
        print("🧹 جاري مسح وثائق السيناريوهات القديمة فقط...")
        delete_legacy_documents("السيناريو المتوقع:", "وثائق السيناريوهات القديمة")
    
    for i, item in enumerate(SCENARIOS_DATA):
        progress = f"[{i+1}/{len(SCENARIOS_DATA)}]"
        print(f"📦 رفع سيناريو: {item['title']}")
        
        try:
            vector = embed_with_retry(item["text"], label=progress, max_attempts=3)
            post_res = insert_legacy_document(item["text"], vector)

            if post_res.status_code in [201, 200]:
                success_count += 1
                print(f"  ✅ {progress} تم حفظ السيناريو بنجاح.")
            else:
                print(f"  ❌ {progress} خطأ في الرفع:", post_res.text[:100])
        except Exception as exc:
            print(f"  ❌ {progress} حدث خطأ غير متوقع: {exc}")
                    
    print(f"\n🎉 تمت العملية بنجاح! تم رفع {success_count} سيناريو. المساعد الآن جاهز للتعامل مع العملاء كبشري محترف.")

if __name__ == "__main__":
    process_and_save_scenarios()
