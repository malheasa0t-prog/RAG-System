def process_and_save_policies():
    print("🚀 جاري إضافة سياسات المتجر إلى الذاكرة (Vector DB)...")
    success_count = 0

    if RESET_POLICY_DOCUMENTS:
        print("🧹 جاري مسح وثائق السياسات القديمة فقط...")
        delete_legacy_documents("القسم:", "وثائق السياسات القديمة")
    
    for i, item in enumerate(POLICIES_DATA):
        progress = f"[{i+1}/{len(POLICIES_DATA)}]"
        print(f"⏳ معالجة: {item['title']}")
        
        try:
            vector = embed_with_retry(item["text"], label=progress, max_attempts=3)
            post_res = insert_legacy_document(item["text"], vector)

            if post_res.status_code in [201, 200]:
                success_count += 1
                print(f"  ✅ {progress} تم حفظ ({item['title']}) بنجاح.")
            else:
                print(f"  ⚠️ {progress} خطأ حفظ:", post_res.text[:100])
        except Exception as exc:
            print(f"  ❌ {progress} خطأ: {exc}")
                    
    print(f"\n🎉 تم الانتهاء! تمت إضافة {success_count} معلومات جديدة لذكاء المساعد.")

if __name__ == "__main__":
    process_and_save_policies()
