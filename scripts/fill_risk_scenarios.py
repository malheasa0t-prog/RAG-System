ALL_RISK_SCENARIOS = RISK_SCENARIOS + ADDITIONAL_RISK_SCENARIOS


def build_content(item):
    return f"{SOURCE_MARKER}\nالعنوان: {item['title']}\n{item['text']}"


def embed_content(content, index):
    try:
        return embed_with_retry(
            content,
            label=f"[{index}]",
            max_attempts=MAX_RETRIES,
            fallback_wait_seconds=20,
        )
    except RuntimeError:
        print(f"توقفنا قبل تعديل قاعدة البيانات. لم يتم حفظ السيناريو رقم {index}.")
        return None


def delete_old_risk_scenarios():
    response = requests.delete(
        f"{SUPABASE_URL}/rest/v1/ai_documents",
        headers=HEADERS,
        params={"content": f"like.{SOURCE_MARKER}*"},
        timeout=REQUEST_TIMEOUT,
    )

    if response.status_code not in {200, 204}:
        print("تعذر حذف النسخة القديمة من سيناريوهات التصحيح:")
        print(response.text[:300])
        return False

    return True


def insert_documents(documents):
    headers = {**HEADERS, "Prefer": "return=minimal"}
    response = requests.post(
        f"{SUPABASE_URL}/rest/v1/ai_documents",
        headers=headers,
        json=documents,
        timeout=REQUEST_TIMEOUT,
    )

    if response.status_code not in {200, 201}:
        print("تعذر حفظ سيناريوهات التصحيح:")
        print(response.text[:300])
        return False

    return True


def save_risk_scenarios():
    print("جاري تجهيز سيناريوهات تصحيح الأخطاء وتحويلها إلى embeddings...")
    documents = []

    for index, item in enumerate(ALL_RISK_SCENARIOS, start=1):
        content = build_content(item)
        try:
            vector = embed_content(content, index)
        except Exception as exc:
            print(f"خطأ غير متوقع أثناء تجهيز السيناريو رقم {index}: {exc}")
            return

        if vector is None:
            print("لم يتم حذف أو تغيير أي بيانات قديمة.")
            return

        documents.append({"content": content, "embedding": vector})
        print(f"تم تجهيز [{index}/{len(ALL_RISK_SCENARIOS)}] {item['title']}")

    if not delete_old_risk_scenarios():
        return

    if not insert_documents(documents):
        return

    print(f"\nتم حفظ {len(documents)} سيناريو تصحيح في قاعدة المتجهات.")


if __name__ == "__main__":
    save_risk_scenarios()
